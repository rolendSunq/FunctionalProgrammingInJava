package java8.inaction.chapter14.part02;

public class HigherOrderFunction {
	/*
	 * 고차 함수 (Higher-order function)
	 * 하나 이상의 동작을 수행하는 함수.
	 * - 하나 이상의 함수를 인수로 받음
	 * - 함수를 결과로 반환
	 * 
	 * 고차 함수의 구현시 어떤 인수가 전달될지 알 수 없으므로 
	 * 인수에 부작용이 발생할 가능성을 염두에 두어야 한다.
	 * 
	 * 함수에서 인수로 받아 사용하면서 코드가 어떤 작업을 수행하고 프로그램 상태를
	 * 어떻게 바꿀지 예측하기 어려워진다. 이로 인해 디버깅도 어려워진다.
	 * 따라서 인수로 전달된 함수가 어떤 부작용을 포함하게 될지 정확하게 문서화하는 것이 좋다.
	 * 물론 부작용이 없다면 가장 좋다.
	 */
}
